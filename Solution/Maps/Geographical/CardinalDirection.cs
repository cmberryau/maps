namespace Maps.Geographical
{
    /// <summary>
    /// Represents cardinal directions
    /// </summary>
    public enum CardinalDirection
    {
        /// <summary>
        /// North
        /// </summary>
        North = 0,

        /// <summary>
        /// North East
        /// </summary>
        NorthEast = 45,

        /// <summary>
        /// East
        /// </summary>
        East = 90,

        /// <summary>
        /// South East
        /// </summary>
        SouthEast = 135,

        /// <summary>
        /// South
        /// </summary>
        South = 180,

        /// <summary>
        /// South West
        /// </summary>
        SouthWest = 225,

        /// <summary>
        /// West
        /// </summary>
        West = 270,

        /// <summary>
        /// North West
        /// </summary>
        NorthWest = 315,
    }
}