namespace Maps.Geometry
{
    /// <summary>
    /// Describes a relative position of an object
    /// </summary>
    public enum RelativePosition
    {
        /// <summary>
        /// To the relative left of the object
        /// </summary>
        Left,

        /// <summary>
        /// To the relative right of the object
        /// </summary>
        Right,

        /// <summary>
        /// Lies on the centre of the object
        /// </summary>
        Centre
    }
}